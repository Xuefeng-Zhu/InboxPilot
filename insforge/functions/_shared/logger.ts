// Request-scoped wrapper
// ---------------------------------------------------------------------------

/**
 * Wrap a function entrypoint's body so that start, end, and error
 * events are emitted automatically. The wrapper:
 *
 *   1. Builds a `RequestContext` and emits an `info` `start` event.
 *   2. Awaits the body. The body may mutate `ctx.org_id` / `ctx.user_id`
 *      as it learns the tenant and caller (e.g. after a successful
 *      JWT verify and `requireOrgMembership`).
 *   3. Emits an `info` `end` event with `duration_ms` and the final
 *      status.
 *   4. On thrown error, emits an `error` event with the serialized
 *      error and re-throws so the caller's existing try/catch can
 *      shape the HTTP response.
 *   5. Adds the `x-request-id` response header to the returned
 *      Response (when it is a real `Response` object) so a support
 *      ticket can include the id and operators can correlate.
 *
 * Usage:
 *
 *   export default async function (req: Request): Promise<Response> {
 *     const ctx = newRequestContext('send-reply', req);
 *     return withRequest(ctx, async () => {
 *       // ... body
 *       return new Response(..., { headers: { ... } });
 *     });
 *   }
 *
 * If the body throws, the wrapper re-throws so the entrypoint can
 * produce a 500 response with its existing error shape. The error
 * event is already in the log stream by the time the entrypoint's
 * catch block runs.
 */
export async function withRequest<T>(
  ctx: RequestContext,
  body: () => Promise<T>,
): Promise<T> {
  const startNs = Date.now();
  log({
    level: 'info',
    ...ctx,
    msg: 'start',
    status: 'running',
  });

  try {
    const result = await body();
    const durationMs = Date.now() - startNs;
    log({
      level: 'info',
      ...ctx,
      msg: 'end',
      status: 'ok',
      duration_ms: durationMs,
    });
    return result;
  } catch (err) {
    const durationMs = Date.now() - startNs;
    logError(ctx, err, {
      msg: 'function error',
      status: 'error',
      duration_ms: durationMs,
    });
    throw err;
  }
}

/**
 * Best-effort: attach `x-request-id` to a Response. Returns the
 * input unchanged when it is not a Response (the body may return
 * a plain object in some tests). Used by `withRequest` callers that
 * want the response header to carry the correlation id.
 */
export function withRequestIdHeader(
  ctx: RequestContext,
  response: Response,
): Response {
  // Clone headers so we never mutate the caller's Response in place
  // (a body of the response can only be read once, but headers are
  // safe to copy).
  const headers = new Headers(response.headers);
  headers.set('x-request-id', ctx.request_id);
  return new Response(response.body, {
    status: response.status,
    statusText: response.statusText,
    headers,
  });
}

// ---------------------------------------------------------------------------
// Min level override (test seam)
// ---------------------------------------------------------------------------

/** Internal: the min-level when not driven by env. */
let overrideMinLevel: number | null = null;

/**
 * Override the minimum log level. Pass `null` to revert to the
 * `LOG_LEVEL` env-var fallback. Tests use this to force-allow
 * `debug` lines without setting an env var.
 */
export function setMinLevel(level: LogLevel | null): void {
  overrideMinLevel = level === null ? null : LEVEL_ORDER[level];
}

/**
 * Returns the active minimum-level threshold. Visible to tests so
 * they can assert the filter actually changed.
 */
export function getMinLevel(): number {
  return overrideMinLevel ?? readMinLevel();
}

/**
 * Exposed for testing only: the override that `setMinLevel` writes.
 * Reads should always go through `getMinLevel()`.
 */
export const _testInternals = {
  get override(): number | null {
    return overrideMinLevel;
  },
};

/** Re-exports the active sink and min-level into one helper for tests. */
export function _resetForTests(): void {
  currentSink = defaultSink();
  overrideMinLevel = null;
}
