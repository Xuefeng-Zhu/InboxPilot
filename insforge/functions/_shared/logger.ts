/**
 * Emit a single structured log event. Always writes one JSON object
 * per line, never concatenated. The event is filtered against the
 * current min-level (set via `LOG_LEVEL` or `setMinLevel`) and then
 * handed to the active sink.
 *
 * This function is the only thing the rest of the codebase should
 * call. Do NOT call `console.log` directly from function entrypoints
 * — going through `log()` keeps the shape consistent and makes the
 * level filter actually work.
 */
export function log(event: LogEvent): void {
  const level: LogLevel = event.level ?? 'info';
  // Use the override min-level (test seam) when set; otherwise fall
  // back to the env-var driven default.
  const min = overrideMinLevel ?? readMinLevel();
  if (LEVEL_ORDER[level] < min) {
    return;
  }

  // Final shape: drop `level` after we use it, replace it with a
  // top-level `level` string. Always keep `timestamp` first so the
  // stream is trivially sorted by eyeball.
  const { level: _omit, ...rest } = event;
  const line = JSON.stringify({
    timestamp: new Date().toISOString(),
    level,
    ...rest,
  });

  switch (level) {
    case 'debug':
      currentSink.debug(line);
      break;
    case 'info':
      currentSink.info(line);
      break;
    case 'warn':
      currentSink.warn(line);
      break;
    case 'error':
      currentSink.error(line);
      break;
  }
}

/**
 * Convenience: log an error event with the canonical error shape.
 * Wraps `log({ level: 'error', ... })` and serializes the thrown
 * value safely.
 */
export function logError(
  ctx: RequestContext,
  err: unknown,
  extra: Record<string, unknown> = {},
): void {
  log({
    level: 'error',
    ...ctx,
    msg: 'function error',
    status: 'error',
    error: serializeError(err),
    ...extra,
  });
}

